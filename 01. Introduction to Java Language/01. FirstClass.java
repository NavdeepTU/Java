/*
 * 	JDK(Java Development Kit) -> JRE(Java Runtime Environment) -> JVM(Java Virtual Machine)
 *	Compilation :
 *		- Source code (.java file) is converted (by JDK) to Byte Code (.class file)
 *		- Byte Code is machine independent.
 *		- Byte Code can run on any machine as long as (the machine) has JRE.
 *	Execution :
 *		- Byte Code is converted into machine's native language (eg. binary) by JVM.
 */


